/*Utilizando a biblioteca Math.
 *Ela possui métodos e constantes estáticas para uso.*/

package br.geekuniversity.secao18_;
public class Programa40 {
	public static void main(String[] args) {
		
		/*Constantes*/
		System.out.println(Math.E);
		System.out.println(Math.PI+"\n");
		
		/*Angulos*/
		System.out.println(Math.sin(45));
		System.out.println(Math.cos(45));
		System.out.println(Math.tan(45)+"\n");
		
		/*Arredondamento*/
		System.out.println(Math.round(Math.PI));
		System.out.println(Math.ceil(Math.PI));
		System.out.println(Math.floor(Math.PI)+"\n");
		
		/*Calculos*/
		System.out.println(Math.pow(3,2));
		System.out.println(Math.sqrt(9));
		
	}
}

















